<h1 class="display-4">SpongeBob SquarePants</h1>

<p>SpongeBob bernama asli Robert Squarepants, anak dari Harold dan Margaret Squarepants. SpongeBob adalah sebuah spons laut, tetapi ia menyerupai spons dapur, berwarna kuning dan berbentuk persegi panjang. SpongeBob memiliki mata biru yang besar, hidung pendek, mulut besar dengan gigi menonjol ke depan, dan lesung dengan tiga bintik-bintik di pipi masing-masing. Dia biasanya mengenakan kemeja putih dengan dasi merah, sepatu kulit hitam dengan tali sepatu, dan celana persegi panjang coklat dengan sabuk hitam (seluruh pakaian yang kadang-kadang disebutnya "celana"). Ketika bekerja di Krusty Krab, ia mengenakan topi, tinggi putih dengan biru jangkar kecil di atasnya sebagai seragam. SpongeBob tidur dengan pakaian jalanan di atas, termasuk sepatunya. Meskipun SpongeBob selalu disuarakan oleh Tom Kenny, suaranya telah berubah selama seri, suaranya lebih rendah dari biasanya dalam episode pilot, dan setelah film, suaranya akan sedikit lebih tinggi melengking.

SpongeBob adalah karakter yang sangat naif, kekanak-kanakan, dan hiperaktif. Dia adalah orang yang sangat ditentukan, dan seringkali akan berhenti pada apapun untuk menyelesaikan tugas. Ia juga agak dramatis kepada orang-orang yang baik kepadanya. Ia juga sangat terlalu percaya diri dan kadang melebih-lebihkan masalah, seperti kehilangan tanda pengenalnya.[12]

SpongeBob sangat baik hati dan polos, dan sangat jarang bertindak secara terbuka kepada siapa pun, bahkan musuh-musuhnya. Dia adalah orang yang sangat mementingkan orang lain dan setia, khususnya terhadap orang-orang yang dekat dengannya. Sifat mementingkan orang lainnya ditampilkan dalam episode "Best Day Ever", di mana dia mengorbankan "hari yang sempurna"-nya untuk membantu teman-temannya. Meskipun niatnya baik, tindakan SpongeBob sering kali mengganggu dan menimbulkan masalah bagi orang-orang di sekitarnya, terutama tetangga sebelahnya, Squidward Tentacles.

Kesalahan SpongeBob juga membuat dia terlalu percaya dan sangat mudah tertipu, dan dia mudah dimanipulasi oleh orang-orang yang berniat untuk menggunakan dan/atau membahayakan dirinya, seperti Squidward, Mr. Krabs dan Plankton; paling jelas, dia selalu mudah tertipu oleh Plankton tidak peduli bagaimana aneh atau jelas penyamarannya. Meskipun ia umumnya baik hati dan santai, ketika frustrasi dan marah, SpongeBob bisa sarkastis, kasar dan 'bermulut kotor' kepada teman-temannya, bahkan Mr. Krabs, yang ia memperlakukan sebagai ayah. Lucunya, Squidward adalah satu-satunya karakter SpongeBob yang tidak pernah dihina ketika ditipu, kadang-kadang dia tetap percaya meskipun Squidward sudah mengaku.</p>
